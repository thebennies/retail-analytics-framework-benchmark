#!/usr/bin/env bash
# Phase 1b — spec section 2. Auto-detect core count, generate
# docker/docker-compose.override.yml with cpuset entries.
set -euo pipefail

SCRIPT_DIR="$(cd "$(dirname "${BASH_SOURCE[0]}")" && pwd)"
REPO_ROOT="$(cd "${SCRIPT_DIR}/.." && pwd)"
OVERRIDE_FILE="${REPO_ROOT}/docker/docker-compose.override.yml"

CORES="$(nproc --all)"
echo "[cpu-pin] detected ${CORES} cores"

case "${CORES}" in
  4)
    PG_CPUSET="0"
    PGB_CPUSET="1"
    SVC_CPUSET="2,3"
    K6_CPUSET="1"
    LAYOUT="4-core (k6 shares with pgbouncer)"
    ;;
  8|16|32)
    HALF=$((CORES / 2))
    PG_CPUSET="0,1"
    PGB_CPUSET="2"
    K6_CPUSET="3"
    SVC_CPUSET="$(seq -s, $((HALF - 1)) $((CORES - 1)))"
    LAYOUT="${CORES}-core"
    ;;
  *)
    echo "WARN: unsupported core count ${CORES}; falling back to 8-core layout" >&2
    PG_CPUSET="0,1"
    PGB_CPUSET="2"
    K6_CPUSET="3"
    SVC_CPUSET="4,5,6,7"
    LAYOUT="fallback 8-core layout"
    ;;
esac

echo "[cpu-pin] layout = ${LAYOUT}"
echo "[cpu-pin] postgres=${PG_CPUSET}, pgbouncer=${PGB_CPUSET}, service=${SVC_CPUSET}, k6=${K6_CPUSET}"

cat > "${OVERRIDE_FILE}" <<YAML
# Auto-generated by scripts/cpu-pin.sh. Do NOT hand-edit; regenerate instead.
# Layout: ${LAYOUT}
services:
  postgres:
    cpuset: "${PG_CPUSET}"
    mem_limit: 4g
  pgbouncer:
    cpuset: "${PGB_CPUSET}"
    mem_limit: 512m
    ulimits:
      nofile:
        soft: 65535
        hard: 65535
  fastapi:
    cpuset: "${SVC_CPUSET}"
    mem_limit: 2g
  axum:
    cpuset: "${SVC_CPUSET}"
    mem_limit: 2g
YAML

# fastify added to override when it appears in base compose (Phase 2b).

echo "${K6_CPUSET}" > "${REPO_ROOT}/.k6-cpuset"
echo "[cpu-pin] wrote ${OVERRIDE_FILE}"
echo "[cpu-pin] wrote .k6-cpuset = ${K6_CPUSET}"
