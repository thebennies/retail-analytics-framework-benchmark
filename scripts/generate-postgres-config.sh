#!/usr/bin/env bash
# Phase 1b — spec section 3. RAM-aware Postgres config generator.
set -euo pipefail

SCRIPT_DIR="$(cd "$(dirname "${BASH_SOURCE[0]}")" && pwd)"
REPO_ROOT="$(cd "${SCRIPT_DIR}/.." && pwd)"
CONF_PATH="${REPO_ROOT}/database/postgresql.conf"

HOST_RAM_MB="$(awk '/MemTotal/{printf "%d", $2/1024}' /proc/meminfo)"
PG_RAM_MB=$(( HOST_RAM_MB < 4096 ? HOST_RAM_MB : 4096 ))
echo "[pg-config] host RAM=${HOST_RAM_MB}MB, postgres container budget=${PG_RAM_MB}MB"

SHARED_BUFFERS_MB=$(( PG_RAM_MB / 4 ))
EFFECTIVE_CACHE_MB=$(( PG_RAM_MB * 3 / 4 ))
WORK_MEM_MB=64
MAINT_MEM_MB=512
MAX_CONN=500

cat > "${CONF_PATH}" <<CONF
# Auto-generated by scripts/generate-postgres-config.sh — spec section 3.
shared_buffers = ${SHARED_BUFFERS_MB}MB
effective_cache_size = ${EFFECTIVE_CACHE_MB}MB
work_mem = ${WORK_MEM_MB}MB
maintenance_work_mem = ${MAINT_MEM_MB}MB
max_connections = ${MAX_CONN}
random_page_cost = 1.1
wal_buffers = 16MB
checkpoint_completion_target = 0.9
synchronous_commit = on
listen_addresses = '*'
CONF

echo "[pg-config] wrote ${CONF_PATH}"
